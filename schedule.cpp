#include "schedule.h"

//**************************************************************************************************
//	Schedule::Schedule
//--------------------------------------------------------------------------------------------------
Schedule::Schedule(TimeStamp _begin, TimeStamp _end)
: begin(_begin)
, end(_end)
{}
//==================================================================================================
//	Schedule::IsIntersected
//--------------------------------------------------------------------------------------------------
bool Schedule::IsIntersected(const Schedule& _other) const
{
	return begin <= _other.end && _other.begin <= end;
}
